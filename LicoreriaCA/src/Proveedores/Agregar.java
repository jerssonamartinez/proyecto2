    public void realizarConexion() {
        String urlDatabase = "jdbc:postgresql://localhost:5432/LicoreraDB";
        try {
            Class.forName("org.postgresql.Driver");
            conn = DriverManager.getConnection(urlDatabase, "postgres", "1234");
        } catch (Exception e) {
            System.out.println("Ocurrio un error : " + e.getMessage());
        }
        if (conn != null) {
            System.out.println("La conexi√≥n se realizo sin problemas! ");
        }
    }

    public void desconectar() {
        try {
            conn.close();
            conn = null;
        } catch (SQLException ex) {
            System.out.println("problema al desconectar la BD");
        }
        System.out.println("conn cerrada");
    }

    public void insertar() {

        if (conn != null) {
            Statement s = null;
            ResultSet I = consulta();
            try {
                adv.setText(advertencia());

                //Si I no es nulo, es porque se arrojo un query por lo tanto. existe un dato con esa id
                String T[] = new String[1];
                while (I.next()) {
                    T[0] = I.getString(1);
                }

                if ((T[0] != null) /*&& (T2[0] == null)*/) {
                    s = conn.createStatement();

                    s.executeQuery("INSERT INTO proveedor "
                            + "(nombreproveedor, telefono,entidad) VALUES ('"
                            + nombreP.getText()
                            + "','" + telP.getText()
                            + "','"+rsP.getText()
                            + "');"
                            + "INSERT INTO compra_has_proveedor "
                            + "(idCompra,idProveedor) VALUES ("
                            + idComp.getText() + "," + T[0] + ")"
                    );
                }
            } catch (Exception ex) {
                System.out.println("insertado");
            }
        }
    }

    public ResultSet consulta() {
        ResultSet n = null;
        try {
            Statement st = conn.createStatement();
            n = st.executeQuery("SELECT idCompra FROM compra WHERE idCompra = " + idComp.getText());

        } catch (SQLException ex) {
            ex.getMessage();
        }
        return n;
    }

    public String advertencia() throws SQLException {
        String S = null;

        ResultSet Q = consulta();
        //T query que consulta si existe un dato con ese cod
        String T[] = new String[1];

        while (Q.next()) {
            T[0] = Q.getString(1);
        }

        if (nombreP.getText().isEmpty() || telP.getText().isEmpty()
                || idComp.getText().isEmpty() || rsP.getText().isEmpty()) {
            S = "<html>Error al guardar el dato. Existen campos vac√≠os</html>";
        } else if (T[0] == null) {
            S = "<html>Error al insertar el dato, NO existe una compra con esa ID</html>";
        } else {
            S = " ";
        }
        return S;
    }
